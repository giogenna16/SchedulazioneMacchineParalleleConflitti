package euristicaClassi;

public class Conflitto {
	
   private int idJob1;
   private int idJob2;
   
public Conflitto(int idJob1, int idJob2) {
	super();
	this.idJob1 = idJob1;
	this.idJob2 = idJob2;
}

public int getIdJob1() {
	return idJob1;
}

public void setIdJob1(int idJob1) {
	this.idJob1 = idJob1;
}

public int getIdJob2() {
	return idJob2;
}

public void setIdJob2(int idJob2) {
	this.idJob2 = idJob2;
}
   
   
   

 

}
